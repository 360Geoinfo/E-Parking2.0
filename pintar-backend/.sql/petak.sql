CREATE TABLE
    "SMARTPARKINGDB"."PETAK" (
        "PETAKID" VARCHAR2 (250 BYTE) PRIMARY KEY,
        "USERID" VARCHAR2 (250 BYTE),
        "USERNAME" VARCHAR2 (250 BYTE),
        "PETAKDIGIT" VARCHAR2 (250 BYTE), -- Balance of Parking Lots Available To Use
        "PAYMENTSTATUS" VARCHAR2 (250 BYTE),
        "PAYMENTAMOUNT" NUMBER (10, 2),
        "PAYMENTMETHOD" VARCHAR2 (250 BYTE),
        "PAYMENTDATE" DATE,
        "IDTRANSACTION" VARCHAR2 (250 BYTE),
        "BUYDATE" DATE,
        "BUYTIME" TIMESTAMP(6)
    );

CREATE TABLE
    "SMARTPARKINGDB"."PETAK_PURCHASES" (
        "IDTRANSACTION" VARCHAR2 (250 BYTE) PRIMARY KEY,
        "PETAKID" VARCHAR2 (250 BYTE),
        "PETAK_AMOUNT" NUMBER, -- Amount of parking lots purchased
        "BUYDATE" DATE,
        "BUYTIME" TIMESTAMP(6)
    );

CREATE TABLE
    "SMARTPARKINGDB"."PETAK_USED" (
        "ID" NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        "PETAK_REF" VARCHAR2 (250 BYTE),
        "OUTDOOR_REF" VARCHAR2 (250 BYTE), -- Reference to the outdoor parking lot
        "PETAK_AMOUNT" NUMBER, -- Amount of parking lots used
        "DATE_USED" VARCHAR2 (250 BYTE)
    );

CREATE TABLE
    "SMARTPARKINGDB"."RECEIPTPETAK" (
        "PETAKID" VARCHAR2 (250 BYTE) PRIMARY KEY,
        "USERID" VARCHAR2 (250 BYTE),
        "USERNAME" VARCHAR2 (250 BYTE),
        "PETAKDIGIT" VARCHAR2 (250 BYTE), -- Balance of Parking Lots Available To Use
        "PAYMENTSTATUS" VARCHAR2 (250 BYTE),
        "PAYMENTAMOUNT" NUMBER (10, 2),
        "PAYMENTMETHOD" VARCHAR2 (250 BYTE),
        "PAYMENTDATE" DATE,
        "IDTRANSACTION" VARCHAR2 (250 BYTE),
        "BUYDATE" DATE,
        "BUYTIME" TIMESTAMP(6)
    );