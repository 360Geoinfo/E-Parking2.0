CREATE TABLE
    "SMARTPARKINGDB"."SAMAN" (
        "SAMANID" VARCHAR2 (250 BYTE) PRIMARY KEY,
        "OUTDOORPARKINGID" VARCHAR2 (250 BYTE),
        "OPERATORID" VARCHAR2 (250 BYTE),
        "SAMANDATETIME" TIMESTAMP DEFAULT (CURRENT_TIMESTAMP, "YYYY-MM-DDTHH24:MI:SS"),
        "STATUS" VARCHAR2 (50 BYTE) DEFAULT 'ACTIVE',
        "CREATED_AT" TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
        "UPDATED_AT" TIMESTAMP DEFAULT CURRENT_TIMESTAMP
    );

CREATE TABLE
    "SMARTPARKINGDB"."SAMAN_PAYMENT" (
        "PAYMENTID" VARCHAR2 (250 BYTE) PRIMARY KEY,
        "SAMANID" VARCHAR2 (250 BYTE),
        "OPERATORID" VARCHAR2 (250 BYTE),
        "AMOUNT" NUMBER (10, 2),
        "PAYMENT_DATE" TIMESTAMP DEFAULT (CURRENT_TIMESTAMP, 'YYYY-MM-DDTHH24:MI:SS'),
        "PAYMENT_METHOD" VARCHAR2 (50 BYTE),
        "PAYMENT_STATUS" VARCHAR2 (50 BYTE) DEFAULT 'PENDING'
    );

CREATE TABLE
    "SMARTPARKINGDB"."PETAK_SAMAN_USAGE" (
        "ID" NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
        "PETAKID" VARCHAR2 (250 BYTE),
        "SAMANID" VARCHAR2 (250 BYTE),
        "PETAKDIGIT" NUMBER,
        "DATE_USED" TIMESTAMP DEFAULT (CURRENT_TIMESTAMP, 'YYYY-MM-DDTHH24:MI:SS')
    );

COMMIT;